export class Pixel {
  public r: number;
  public g: number;
  public b: number;
  public a: number;

  constructor(r: number, g: number, b: number, a: number) {
    this.r = r;
    this.g = g;
    this.b = b;
    this.a = a;
  }
}

export interface LevelSource {
  pixelData: Pixel[];
  width: number;
  height: number;
}
